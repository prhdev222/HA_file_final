        for dept in departments:
            print(f"\nüìã {dept['name']} (ID: {dept['id']}, Code: {dept['code']})")
            print(f"   ‡∏Ñ‡∏≥‡∏≠‡∏ò‡∏¥‡∏ö‡∏≤‡∏¢: {dept['description']}")
            print(f"   ‡∏ß‡∏±‡∏ô‡∏ó‡∏µ‡πà‡∏™‡∏£‡πâ‡∏≤‡∏á: {dept['created_at']}")
